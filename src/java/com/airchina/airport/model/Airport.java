package com.airchina.airport.model;

public class Airport {

	private String airportCode;
	private String airportName;
	private String cityName;
	private String internationalFlag;
	private String citySort;
	private String longitude;
	private String latitude;
	public String getAirportCode() {
		return airportCode;
	}
	public void setAirportCode(String airportCode) {
		this.airportCode = airportCode;
	}
	public String getAirportName() {
		return airportName;
	}
	public void setAirportName(String airportName) {
		this.airportName = airportName;
	}
	public String getCityName() {
		return cityName;
	}
	public void setCityName(String cityName) {
		this.cityName = cityName;
	}
	public String getInternationalFlag() {
		return internationalFlag;
	}
	public void setInternationalFlag(String internationalFlag) {
		this.internationalFlag = internationalFlag;
	}
	public String getCitySort() {
		return citySort;
	}
	public void setCitySort(String citySort) {
		this.citySort = citySort;
	}
	public String getLongitude() {
		return longitude;
	}
	public void setLongitude(String longitude) {
		this.longitude = longitude;
	}
	public String getLatitude() {
		return latitude;
	}
	public void setLatitude(String latitude) {
		this.latitude = latitude;
	}
	
	
	
	
}
